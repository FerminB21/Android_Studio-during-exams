public class MapsActivity extends FragmentActivity implements OnMapReadyCallback {

    private GoogleMap mMap;
    private static final LatLng l1=new LatLng(37.790846,26.6880529);

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_maps);
        SupportMapFragment mapFragment = (SupportMapFragment) getSupportFragmentManager()
                .findFragmentById(R.id.map);
        mapFragment.getMapAsync(this);
    }


   @Override
    public void onMapReady(GoogleMap googleMap) {
        mMap = googleMap;

        // Add a marker
        LatLng karlovasi = new LatLng(37.790846, 26.6880529);
        mMap.addMarker(new MarkerOptions().position(karlovasi).title("Marker in Karlobasi"));
        Marker marker=mMap.addMarker(new MarkerOptions().position(karlovasi));
        marker.setVisible(true);
        mMap.moveCamera(CameraUpdateFactory.newLatLng(karlovasi));
    }
}
